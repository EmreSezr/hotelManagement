    public void showDateIntoTable() {
        try {
            // Müşteri tablosundan "status" alanı "NULL" olan kayıtları seçer
            pst = con.prepareStatement("select * from customer where status=?");
            pst.setString(1, "NULL");
            rs = pst.executeQuery();

            // Sonuç kümesinin meta verilerini alır
            ResultSetMetaData ob = rs.getMetaData();
            int n = ob.getColumnCount(); // Sütun sayısını alır

            DefaultTableModel df = (DefaultTableModel) jTable1.getModel(); // Tablo modelini alır
            df.setRowCount(0); // Tablonun satır sayısını sıfırlar

            while (rs.next()) {
                Vector v = new Vector(); // Yeni bir vektör oluşturur
                // Sütunlardaki verileri vektöre ekler
                for (int i = 1; i <= n; i++) {
                    v.add(rs.getString("name"));
                    v.add(rs.getString("mobile"));
                    v.add(rs.getString("id"));
                    v.add(rs.getString("roomnumber"));
                    v.add(rs.getString("bed"));
                    v.add(rs.getString("roomtype"));
                    v.add(rs.getString("joindate"));
                    v.add(rs.getString("billid"));
                    v.add(rs.getString("nationality"));
                }
                df.addRow(v); // Vektörü tabloya ekler
            }

        } catch (SQLException ex) {
            // Veritabanı hatası oluşursa log kaydı oluşturulur
            Logger.getLogger(checkout.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

