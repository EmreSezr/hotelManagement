    private void btnSearchRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchRoomActionPerformed
        try {
            // Veritabanından oda numarasına göre müşteri bilgilerini arar
            pst = con.prepareStatement("select * from customer where roomnumber=? and status=?");
            pst.setString(1, txtroomnumber.getText()); // Arama kriteri olarak oda numarasını belirler
            pst.setString(2, "NULL"); // Müşterinin çıkış yapmadığı durumu belirler
            rs = pst.executeQuery(); // Sorguyu çalıştırır
            if (rs.next()) {
                // Eğer müşteri bulunursa, müşteri bilgilerini alır
                txtcustomername.setText(rs.getString("name")); // Müşteri adını gösterir
                txtmobile.setText(rs.getString("mobile")); // Müşteri telefon numarasını gösterir
                txttcno.setText(rs.getString("id")); // Müşteri kimlik numarasını gösterir
                txtprice.setText(rs.getString("price")); // Oda ücretini gösterir
                txtindate.setText(rs.getString("joindate")); // Giriş tarihini gösterir

                // Müşterinin çıkış tarihi ve giriş tarihleri arasındaki gün farkını hesaplıyoruz.
                String s1, s2;
                s1 = txtoutdate.getText(); // Çıkış tarihini alır(Bugün olarak)
                s2 = rs.getString("joindate"); // Giriş tarihini alır
                SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");

                Date d1 = format.parse(s1); // Çıkış tarihini tarih nesnesine dönüştürür
                Date d2 = format.parse(s2); // Giriş tarihini tarih nesnesine dönüştürür
                long tarihFarki = d1.getTime() - d2.getTime(); // Gün farkını hesaplar
                // Gün farkını tarih cinsinden integer gün cinsine dönüştürür
                int days = (int) tarihFarki / (24 * 60 * 60 * 1000); 
                if (days == 0) {
                    // Eğer gün farkı 0 ise, minimum bir gün olarak kabul eder
                    // Yani aynı gün içinde giriş-çıkış yapan olursa gün=1 olur.
                    days = 1; 
                }
                txtdays.setText(String.valueOf(days)); // Kalış süresini gösterir
                // Toplam ödenecek miktarı hesaplar (amount=günlük ücret(price) * toplam kalış(days) )
                double price = Double.parseDouble(rs.getString("price")) * days;
                txtamount.setText(String.valueOf(price)); // Toplam ödenecek miktarı gösterir
            }

        } catch (SQLException ex) {
            Logger.getLogger(checkout.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(checkout.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnSearchRoomActionPerformed

